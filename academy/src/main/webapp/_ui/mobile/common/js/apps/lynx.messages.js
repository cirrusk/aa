// $.getMsg("Products Average Rating {0} / {1}", "1,5");
// return : Products Average Rating 1 / 5
$.getMsg = function(message, arguments) {
	if(arguments == null) 
		return message;
	
	if($.isArray(arguments) == false) {
		arguments = arguments.toString().split(",");
	}
	var param = {};
	for(var idx in arguments) {
		var key = "{" + idx + "}";
		var value = $.trim(arguments[idx]);
		param[key] = value;
	}
	for (var val in param) {
		message = message.split(val).join(param[val]);
	}
	return message;
};
